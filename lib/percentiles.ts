function assertPercentileTable() {
  for (let index = 1; index < derivationCohortPercentiles.length; index += 1) {
    const previous = derivationCohortPercentiles[index - 1];
    const current = derivationCohortPercentiles[index];

    if (current.percentile < previous.percentile) {
      throw new Error("Derivation percentile table must be ordered by percentile.");
    }

    if (current.linearPredictorCutpoint < previous.linearPredictorCutpoint) {
      throw new Error(
        "Derivation percentile table must be ordered by linear predictor cutpoint.",
      );
    }
  }
}

assertPercentileTable();

export function calculateDerivationPercentile(linearPredictor: number): number {
  const first = derivationCohortPercentiles[0];
  const last = derivationCohortPercentiles[derivationCohortPercentiles.length - 1];

  // Clamp LP values that fall outside the observed derivation distribution.
  if (linearPredictor <= first.linearPredictorCutpoint) {
    return first.percentile;
  }

  if (linearPredictor >= last.linearPredictorCutpoint) {
    return last.percentile;
  }

  for (let index = 1; index < derivationCohortPercentiles.length; index += 1) {
    const lower = derivationCohortPercentiles[index - 1];
    const upper = derivationCohortPercentiles[index];

    if (linearPredictor === upper.linearPredictorCutpoint) {
      return upper.percentile;
    }

    if (linearPredictor < upper.linearPredictorCutpoint) {
      // Interpolate within the enclosing LP interval to preserve a smooth
      // percentile scale between adjacent derivation cutpoints.
      const span =
        upper.linearPredictorCutpoint - lower.linearPredictorCutpoint;
      const fraction =
        span === 0 ? 0 : (linearPredictor - lower.linearPredictorCutpoint) / span;

      return lower.percentile + fraction * (upper.percentile - lower.percentile);
    }
  }

  return last.percentile;
}
